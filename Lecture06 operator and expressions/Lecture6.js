// operators
// types of operators in javascript

// Arithmetic operator
let a = 10;
let b = 20;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a ** b);
console.log(a % b);
console.log(a++);
console.log(a--);
console.log(++a);
console.log(--a);

// Assignment operator
let c = 10;
let d = 20;

console.log(c += d);
console.log(c -= d);
console.log(c *= d);
console.log(c/= d);
console.log(c **= d);
console.log(c %= d);

// Comparison operator
let e = 10;
let f = 20;
console.log(e == f);
console.log(e != f);
console.log(e > f);
console.log(e < f);
console.log(e >= f);
console.log(e <= f); 
console.log(e === f); // equal type and equal value
console.log(e !== f); // equal type and not equal value

// Logical operator
let g = 10;
let h = 20;
console.log(g < h && g > h);
console.log(g < h || g > h);
console.log(!(g < h && g > h));

// Bitwise operator
let i = 10;
let j = 20;
console.log(i & j);
console.log(i | j);
console.log(i ^ j);
console.log(~i);
console.log(i << j);
console.log(i >> j);
console.log(i >>> j);

// typeof operator
let k = 10;
console.log(typeof k);
console.log(typeof "k");
console.log(typeof true);

// delete operator
let l = 10;
console.log(delete l);

// in operator
let m = {name: "Rahul"};
console.log("name" in m);

console.log(!true)
console.log(!false)


